    public function store($user, array $data)
    {
        $this->validateBookingData($user, $data);

        $data = $this->setDefaultValues($data, $data['immediate'] === 'yes');
        $data['due'] = $this->setDueDate($data);
        $data['job_type'] = $this->getJobTypeByConsumerType($user->userMeta->consumer_type);
        $this->setJobAttributes($data);

        if (isset($data['due'])) {
            $data['will_expire_at'] = TeHelper::willExpireAt($data['due'], $data['b_created_at']);
        }

        $job = $this->bookingRepository->createCurserJob($user, $data);
        return $this->prepareJobData($job, $data);
    }
    private function setDueDate(array $data): string
    {
        if ($data['immediate'] === 'yes') {
            return Carbon::now()->addMinutes(5)->toDateTimeString();
        }

        $due = Carbon::createFromFormat('m/d/Y H:i', "{$data['due_date']} {$data['due_time']}");
        if ($due->isPast()) {
            throw new \Exception("Cannot create a booking in the past.");
        }

        return $due->toDateTimeString();
    }
    
    private function validateBookingData($user, array $data)
    {
        if ($user->user_type !== env('CUSTOMER_ROLE_ID')) {
            throw new \Exception("Only customers can create bookings.");
        }

        if (!isset($data['from_language_id'])) {
            throw new \Exception("Missing required field: from_language_id.");
        }

        if ($data['immediate'] === 'no') {
            $requiredFields = ['due_date', 'due_time', 'duration'];
            foreach ($requiredFields as $field) {
                if (empty($data[$field])) {
                    throw new \Exception("Missing required field: $field.");
                }
            }

            if (empty($data['customer_phone_type']) && empty($data['customer_physical_type'])) {
                throw new \Exception("A choice for customer_phone_type is required.");
            }
        }
    }
    
    private function setDefaultValues(array $data, bool $isImmediate): array
    {
        $data['customer_phone_type'] = $isImmediate ? 'yes' : ($data['customer_phone_type'] ?? 'no');
        $data['customer_physical_type'] = $data['customer_physical_type'] ?? 'no';
        return $data;
    }

    
    private function getJobTypeByConsumerType($consumer_type)
    {
        if ($consumer_type == 'rwsconsumer') {
            return 'rws';
        } elseif ($consumer_type == 'ngo') {
            return 'unpaid';
        } elseif ($consumer_type == 'paid') {
            return 'paid';
        }
        return 'regular'; // Default if no match
    }
    
    private function setJobAttributes(array &$data)
    {
        $jobFor = $data['job_for'] ?? [];
        $data['gender'] = in_array('male', $jobFor) ? 'male' : (in_array('female', $jobFor) ? 'female' : null);
        $data['certified'] = $this->getCertificationType($jobFor);
    }
    

    private function getCertificationType(array $jobFor): ?string
    {
        return match (true) {
            in_array('normal', $jobFor) && in_array('certified', $jobFor) => 'both',
            in_array('normal', $jobFor) && in_array('certified_in_law', $jobFor) => 'n_law',
            in_array('normal', $jobFor) && in_array('certified_in_health', $jobFor) => 'n_health',
            in_array('certified', $jobFor) => 'yes',
            in_array('certified_in_law', $jobFor) => 'law',
            in_array('certified_in_health', $jobFor) => 'health',
            in_array('normal', $jobFor) => 'normal',
            default => null,
        };
    }
    
    private function prepareJobData($job, $data)
    {
        // This method will prepare the job data for returning
        $data['id'] = $job->id;
        $data['job_for'] = $this->prepareJobFor($job);
        $data['customer_town'] = $job->userMeta->city;
        $data['customer_type'] = $job->userMeta->customer_type;
        
        return $data;
    }
    
    private function prepareJobFor($job)
    {
        // Prepare job_for data based on the job's gender and certified status
        $jobFor = [];
    
        if ($job->gender != null) {
            $jobFor[] = $job->gender === 'male' ? 'Man' : 'Kvinna';
        }
    
        if ($job->certified != null) {
            if ($job->certified === 'both') {
                $jobFor[] = 'normal';
                $jobFor[] = 'certified';
            } else {
                $jobFor[] = $job->certified;
            }
        }
    
        return $jobFor;
    }

    public function updateJob($id, $data, $cuser)
    {
        $job = $this->jobRepository->getJobByID($id);

        $current_translator = $this->jobRepository->getJobTranslatorCancelAt($job);
        if (is_null($current_translator))
            $current_translator = $this->jobRepository->getJobTranslatorCompletedAt($job);

        $log_data = [];

        $langChanged = false;

        $changeTranslator = $this->changeTranslator($current_translator, $data, $job);
        if ($changeTranslator['translatorChanged']) $log_data[] = $changeTranslator['log_data'];

        $changeDue = $this->changeDue($job->due, $data['due']);
        if ($changeDue['dateChanged']) {
            $old_time = $job->due;
            $job->due = $data['due'];
            $log_data[] = $changeDue['log_data'];
        }

        if ($job->from_language_id != $data['from_language_id']) {
            $log_data[] = [
                'old_lang' => TeHelper::fetchLanguageFromJobId($job->from_language_id),
                'new_lang' => TeHelper::fetchLanguageFromJobId($data['from_language_id'])
            ];
            $old_lang = $job->from_language_id;
            $job->from_language_id = $data['from_language_id'];
            $langChanged = true;
        }

        $changeStatus = $this->changeStatus($job, $data, $changeTranslator['translatorChanged']);
        if ($changeStatus['statusChanged'])
            $log_data[] = $changeStatus['log_data'];

        $job->admin_comments = $data['admin_comments'];

        $this->logger->addInfo('USER #' . $cuser->id . '(' . $cuser->name . ')' . ' has been updated booking <a class="openjob" href="/admin/jobs/' . $id . '">#' . $id . '</a> with data:  ', $log_data);

        $job->reference = $data['reference'];

        if ($job->due <= Carbon::now()) {
            $job->save();
            return ['Updated'];
        } else {
            $job->save();
            if ($changeDue['dateChanged']) $this->sendChangedDateNotification($job, $old_time);
            if ($changeTranslator['translatorChanged']) $this->sendChangedTranslatorNotification($job, $current_translator, $changeTranslator['new_translator']);
            if ($langChanged) $this->sendChangedLangNotification($job, $old_lang);
        }
    }

     /**
     * @param $current_translator
     * @param $data
     * @param $job
     * @return array
     */
    private function changeTranslator($current_translator, $data, $job)
    {
        $translatorChanged = false;

        if (!is_null($current_translator) || (isset($data['translator']) && $data['translator'] != 0) || $data['translator_email'] != '') {
            $log_data = [];
            if (!is_null($current_translator) && ((isset($data['translator']) && $current_translator->user_id != $data['translator']) || $data['translator_email'] != '') && (isset($data['translator']) && $data['translator'] != 0)) {
                if ($data['translator_email'] != '') $data['translator'] = $this->bookingRepository->getUserEmail($data);
                $new_translator = $current_translator->toArray();
                $new_translator['user_id'] = $data['translator'];
                unset($new_translator['id']);
                $new_translator = $this->translatorRepository->createTranslator($new_translator);
                $current_translator->cancel_at = Carbon::now();
                $current_translator->save();
                $log_data[] = [
                    'old_translator' => $current_translator->user->email,
                    'new_translator' => $new_translator->user->email
                ];
                $translatorChanged = true;
            } elseif (is_null($current_translator) && isset($data['translator']) && ($data['translator'] != 0 || $data['translator_email'] != '')) {
                if ($data['translator_email'] != '') $data['translator'] = User::where('email', $data['translator_email'])->first()->id;
                $new_translator = $this->translatorRepository->createTranslator(['user_id' => $data['translator'], 'job_id' => $job->id]);
                $log_data[] = [
                    'old_translator' => null,
                    'new_translator' => $new_translator->user->email
                ];
                $translatorChanged = true;
            }
            if ($translatorChanged)
                return ['translatorChanged' => $translatorChanged, 'new_translator' => $new_translator, 'log_data' => $log_data];
        }
        return ['translatorChanged' => $translatorChanged];
    }

