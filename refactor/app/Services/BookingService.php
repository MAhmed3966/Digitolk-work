            'by_admin' => $data['by_admin'] === 'true' ? 'yes' : 'no',
        ];

        try {
            // Update Distance if necessary
            if (!empty($distanceData['distance']) || !empty($distanceData['time'])) {
                $this->distanceRepository->updateDistanceByJobId($data['jobid'], $distanceData);
            }

            // Update Job if necessary
            if (!empty($jobData['admin_comments']) || !empty($jobData['session_time']) || 
                !empty($jobData['flagged']) || !empty($jobData['manually_handled']) || 
                !empty($jobData['by_admin'])) {
                $this->jobRepository->updateJobById($data['jobid'], $jobData);
            }
        } catch (Exception $e) {
            // Rethrow exceptions to let the controller handle them
            throw new Exception("Failed to update job details: " . $e->getMessage());
        }

        return ['status' => 'success'];
    }


