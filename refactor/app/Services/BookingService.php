    public function getUsersJobs(int $userId): array
    {
        $cuser = $this->bookingRepository->getUserByID($userId);
        if (!$cuser) {
            return ['emergencyJobs' => [], 'normalJobs' => [], 'cuser' => null, 'usertype' => null];
        }

        $usertype = $cuser->is('customer') ? 'customer' : 'translator';
        $jobs = $usertype === 'customer'
            ? $this->jobRepository->getJobsWithCurser($cuser)
            : $this->jobRepository->allJobs($this->jobRepository->getTranslatorJobs($cuser->id, 'new'));

        $groupedJobs = $this->groupJobs($jobs, $userId);

        return array_merge($groupedJobs, ['cuser' => $cuser, 'usertype' => $usertype]);
    }


    private function groupJobs(array $jobs, int $userId): array
    {
        $emergencyJobs = [];
        $normalJobs = [];

        foreach ($jobs as $job) {
            if ($job->immediate === 'yes') {
                $emergencyJobs[] = $job;
            } else {
                $normalJobs[] = $job;
            }
        }

        $normalJobs = collect($normalJobs)
            ->each(fn($job) => $job['usercheck'] = $this->jobRepository->checkParticularJob($userId, $job))
            ->sortBy('due')
            ->all();

        return ['emergencyJobs' => $emergencyJobs, 'normalJobs' => $normalJobs];
    }

