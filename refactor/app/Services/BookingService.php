    public function endJob(array $postData)
    {
        $completedDate = now();
        $jobId = $postData['job_id'];

        $jobDetail = $this->bookingRepository->findJobWithRelations($jobId);

        if ($jobDetail->status != 'started') {
            return ['status' => 'success'];
        }

        $dueDate = $jobDetail->due;
        $interval = $this->calculateInterval($dueDate, $completedDate);

        $jobDetail->end_at = $completedDate;
        $jobDetail->status = 'completed';
        $jobDetail->session_time = $interval;

        $user = $this->bookingRepository->getJobUser($jobDetail);
        $email = $jobDetail->user_email ?? $user->email;

        $this->sendSessionEndedEmail($user, $jobDetail, $interval, $email, 'faktura');

        $this->bookingRepository->saveJob($jobDetail);

        $translator = $this->bookingRepository->getJobTranslator($jobDetail);
        $translator->completed_at = $completedDate;
        $translator->completed_by = $postData['user_id'];

        $this->bookingRepository->saveTranslator($translator);

        Event::fire(new SessionEnded($jobDetail, $postData['user_id'] == $jobDetail->user_id ? $translator->user_id : $jobDetail->user_id));

        $translatorUser = $translator->user()->first();
        $this->sendSessionEndedEmail($translatorUser, $jobDetail, $interval, $translatorUser->email, 'lön');

        return ['status' => 'success'];
    }

    private function calculateInterval($start, $end)
    {
        $start = date_create($start);
        $end = date_create($end);
        $diff = date_diff($end, $start);
        return sprintf('%02d:%02d:%02d', $diff->h, $diff->i, $diff->s);
    }

    private function sendSessionEndedEmail($user, $job, $sessionTime, $email, $forText)
    {
        $subject = 'Information om avslutad tolkning för bokningsnummer # ' . $job->id;
        $sessionExplode = explode(':', $sessionTime);
        $formattedTime = $sessionExplode[0] . ' tim ' . $sessionExplode[1] . ' min';

        $data = [
            'user'         => $user,
            'job'          => $job,
            'session_time' => $formattedTime,
            'for_text'     => $forText,
        ];

        $this->mailer->send($email, $user->name, $subject, 'emails.session-ended', $data);
    }

